"""
Compound taxonomy and SMILES catalog for the Chemistry Recognizer project.

This module is STANDALONE: it has no imports from `src/` and can be safely
imported by both `data/generate_dataset.py` and notebooks/scripts.

Public API:
    COMPOUNDS : list[dict]                       — flat list of every compound
    TAXONOMY  : dict                              — nested structure for UI navigation
    get_compounds(category, subcategories, difficulty) -> list[dict]

Every compound dict has the same set of keys; see the comment block below
for the schema.
"""

from typing import Iterable, Optional, List, Dict

# ---------------------------------------------------------------------------
# Schema (per compound):
#   id                    str  — unique slug, lowercase, filesystem-safe
#   name_es               str  — Spanish name (ASCII, no accents)
#   name_display          str  — Spanish display name (with accents)
#   name_iupac            str  — IUPAC / nomenclatura sistemática
#   formula               str  — ASCII formula
#   formula_display       str  — formula with Unicode subscripts
#   smiles                str  — valid RDKit SMILES
#   category              str  — "inorganica" | "organica"
#   subcategory           str  — filesystem-safe slug, key in TAXONOMY
#   subcategory_display   str  — Spanish display name with accents
#   difficulty            str  — "basico" | "intermedio" | "avanzado"
#   nomenclature_note     str  — brief educational note (Spanish)
#   ionic                 bool — True forces formula-text rendering
# ---------------------------------------------------------------------------


def _C(**kw) -> Dict:
    """Helper to build a compound dict with all required keys present."""
    kw.setdefault("ionic", False)
    return kw


