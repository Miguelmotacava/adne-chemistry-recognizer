# ---------------------------------------------------------------------------
# PUBLIC: TAXONOMY (nested for UI navigation)
# ---------------------------------------------------------------------------

def _build_taxonomy(compounds: List[Dict]) -> Dict:
    """Build the nested TAXONOMY structure from the flat compound list."""
    tax: Dict = {}
    for c in compounds:
        cat = c["category"]
        sub = c["subcategory"]
        if cat not in tax:
            # We'll set display name from the first compound of that category.
            display = "Química Inorgánica" if cat == "inorganica" else "Química Orgánica"
            tax[cat] = {"display": display, "subcategories": {}}
        if sub not in tax[cat]["subcategories"]:
            tax[cat]["subcategories"][sub] = {
                "display": c["subcategory_display"],
                "ids": [],
            }
        tax[cat]["subcategories"][sub]["ids"].append(c["id"])
    return tax


TAXONOMY: Dict = _build_taxonomy(COMPOUNDS)


# ---------------------------------------------------------------------------
# PUBLIC: get_compounds(...) filter
# ---------------------------------------------------------------------------

def get_compounds(category: Optional[str] = None,
                  subcategories: Optional[Iterable[str]] = None,
                  difficulty: Optional[Iterable[str]] = None) -> List[Dict]:
    """
    Filter the COMPOUNDS list.

    Args:
        category:     "inorganica" | "organica" | None (= both)
        subcategories: iterable of subcategory slugs (e.g. {"alcanos", "alquenos"})
                       None = all subcategories of the chosen category
        difficulty:   iterable of difficulty levels, or a single string
                       (e.g. "basico", or ["basico", "intermedio"])
                       None = all difficulties

    Returns:
        A new list of compound dicts (does not modify COMPOUNDS).
    """
    if isinstance(difficulty, str):
        difficulty = [difficulty]
    if isinstance(subcategories, str):
        subcategories = [subcategories]

    subset = COMPOUNDS
    if category is not None:
        subset = [c for c in subset if c["category"] == category]
    if subcategories is not None:
        wanted = set(subcategories)
        subset = [c for c in subset if c["subcategory"] in wanted]
    if difficulty is not None:
        wanted_d = set(difficulty)
        subset = [c for c in subset if c["difficulty"] in wanted_d]
    return subset


# ---------------------------------------------------------------------------
# Validation helpers (used by tests)
# ---------------------------------------------------------------------------

REQUIRED_KEYS = {
    "id", "name_es", "name_display", "name_iupac", "formula", "formula_display",
    "smiles", "category", "subcategory", "subcategory_display", "difficulty",
    "nomenclature_note", "ionic",
}


def validate_schema() -> List[str]:
    """Return a list of error strings (empty if every compound is well-formed)."""
    errors: List[str] = []
    seen_ids = set()
    for i, c in enumerate(COMPOUNDS):
        missing = REQUIRED_KEYS - set(c.keys())
        if missing:
            errors.append(f"[#{i} id={c.get('id','?')}] missing keys: {missing}")
        if c["id"] in seen_ids:
            errors.append(f"[#{i}] duplicate id: {c['id']!r}")
        seen_ids.add(c["id"])
        if c["category"] not in {"inorganica", "organica"}:
            errors.append(f"[#{i} id={c['id']}] bad category: {c['category']!r}")
        if c["difficulty"] not in {"basico", "intermedio", "avanzado"}:
            errors.append(f"[#{i} id={c['id']}] bad difficulty: {c['difficulty']!r}")
    return errors


if __name__ == "__main__":
    errors = validate_schema()
    if errors:
        print(f"VALIDATION FAILED — {len(errors)} error(s):")
        for e in errors:
            print("  -", e)
    else:
        print(f"OK — {len(COMPOUNDS)} compounds, schema valid.")
        print(f"     inorganica: {sum(1 for c in COMPOUNDS if c['category']=='inorganica')}")
        print(f"     organica:   {sum(1 for c in COMPOUNDS if c['category']=='organica')}")
        print(f"     subcategorias: {sum(len(v['subcategories']) for v in TAXONOMY.values())}")
